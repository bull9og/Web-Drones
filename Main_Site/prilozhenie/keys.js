document.addEventListener('keydown', function(event) {
	switch (event.key) {
	  case 'w':
		// изменяем параметры и стили для клавиши "W"
		document.getElementById('keyW').style.color = '#D9D9D9';
		document.getElementById('arrow').style.fill = '#1E1E1E';
		break;
	  case 'a':
		// изменяем параметры и стили для клавиши "A"
		document.getElementById('keyA').style.color = '#D9D9D9';
		document.getElementById('arrow2').style.fill = '#1E1E1E';
		break;
	  case 's':
		// изменяем параметры и стили для клавиши "S"
		document.getElementById('keyS').style.color = '#D9D9D9';
		document.getElementById('arrow3').style.fill = '#1E1E1E';
		break;
	  case 'd':
		// изменяем параметры и стили для клавиши "D"
		document.getElementById('keyD').style.color = '#D9D9D9';
		document.getElementById('arrow4').style.fill = '#1E1E1E';
		break;
	}
  });
